#include "push_swap.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}

int	parser(t_stack *stack, char **av)
{
	if ((check_invalid_arg(av) == true)
		|| (check_if_sort(stack) == true)
		|| (check_if_dup(stack) == true))
	{
		ft_putendl_fd("ERROR", 2);
		return (false);
	}
	return (true);
}

int	push_swap(char **av)
{
	t_data	*data;

	data = init_struct(av);
	if (parser(data->a, av) == true)
	{
	// debug_ps(*data);
		get_index(data);
		if (ps_lst_size(data->a) == 2)
			sort_2(data);
		else if (ps_lst_size(data->a) == 3)
			sort_3(data);
		else if (ps_lst_size(data->a) == 4)
			sort_4(data, 0);
		else if (ps_lst_size(data->a) == 5)
			sort_5(data);
		else if (ps_lst_size(data->a) <= 100)
			chunky_sort(data, 27);
		else
			chunky_sort(data, 51);
	}
	// debug_ps(*data);
	free_stack(data->a);
	free_stack(data->b);
	free(data);
	return (0);
}

int	main(int ac, char **av)
{
	if (ac == 1)
		return (0);
	push_swap(av);
	return (0);
}

